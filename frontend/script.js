let currentLang = 'ru';

function updateContent(lang) {
    const elements = document.querySelectorAll('[data-key]');
    const langData = translations[lang];

    elements.forEach(element => {
        const key = element.getAttribute('data-key');
        if (langData[key] !== undefined) {
            
            // 1. –û–±—Ä–∞–±–æ—Ç–∫–∞ INPUT placeholders
            if ((element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') && element.hasAttribute('placeholder')) {
                element.setAttribute('placeholder', langData[key]);
            } 
            
            // 2. –û–±—Ä–∞–±–æ—Ç–∫–∞ LI (Pricing Features)
            else if (element.tagName === 'LI' && element.hasAttribute('content')) {
                const symbol = element.getAttribute('content');
                element.textContent = symbol + ' ' + langData[key];
            } 
            
            // 3. –û–±—Ä–∞–±–æ—Ç–∫–∞ –∫–Ω–æ–ø–æ–∫ '–ü–æ–¥—Ä–æ–±–Ω–µ–µ/–°–∫—Ä—ã—Ç—å'
            else if (element.classList.contains('js-toggle-details')) {
                 const targetId = element.getAttribute('data-target');
                 const targetElement = document.getElementById(targetId);
                 
                 if (targetElement && targetElement.classList.contains('is-open')) {
                    element.textContent = langData['btn_hide'];
                 } else {
                    element.textContent = langData['btn_more'];
                 }
            } 
            
            // 4. –û–±—Ä–∞–±–æ—Ç–∫–∞ —Å—Å—ã–ª–æ–∫ –ø–æ–¥ –º–æ–¥–∞–ª—å–Ω—ã–º–∏ –æ–∫–Ω–∞–º–∏ (—Å–æ —Å—Å—ã–ª–∫–∞–º–∏ –≤–Ω—É—Ç—Ä–∏ —Ç–µ–∫—Å—Ç–∞)
            else if (element.classList.contains('modal-text')) {
                const linkKey = element.querySelector('a').getAttribute('data-key');
                const linkText = langData[linkKey];
                const textWithLink = langData[key].replace('{link}', `<a href="#" class="js-open-modal" data-target-modal="${linkKey.includes('signup') ? 'signup-modal' : 'login-modal'}">${linkText}</a>`);
                element.innerHTML = textWithLink;
            } 
            
            // 5. –û–±—Ä–∞–±–æ—Ç–∫–∞ –≤—Å–µ—Ö –æ—Å—Ç–∞–ª—å–Ω—ã—Ö —Ç–µ–∫—Å—Ç–æ–≤—ã—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ (H1, P, A, BUTTON, TITLE, SPAN, etc.)
            else {
                const content = langData[key];
                // –û–±—Ä–∞–±–æ—Ç–∫–∞ –ø–µ—Ä–µ–Ω–æ—Å–æ–≤ —Å—Ç—Ä–æ–∫ –¥–ª—è —ç–ª–µ–º–µ–Ω—Ç–æ–≤, –≥–¥–µ —ç—Ç–æ –≤–∞–∂–Ω–æ (–Ω–∞–ø—Ä–∏–º–µ—Ä, subtitle)
                if (key.includes('subtitle_line1')) {
                     element.innerHTML = content.replace(/\n/g, '<br>');
                } else {
                     element.textContent = content;
                }
            }
        }
    });
    // –û–±–Ω–æ–≤–ª—è–µ–º –æ—Ç–æ–±—Ä–∞–∂–∞–µ–º—ã–π —è–∑—ã–∫ –≤ —Ö–µ–¥–µ—Ä–µ
    document.querySelector('.current-lang').textContent = lang.toUpperCase();
    document.documentElement.lang = lang;
}

